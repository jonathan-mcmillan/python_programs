from cs1graphics import *
win = Canvas()
win.setBackgroundColor('skyblue')
win.setTitle("Animals")
win.setWidth(400)
win.setHeight(300)

grass = Rectangle(400,100)
grass.setFillColor('green')
win.add(grass)
grass.move(200,250)

tree = Layer()
trunk = Path(Point(0,0), Point(0,-20))
trunk.setBorderWidth(7)
trunk.setBorderColor('brown')
tree.add(trunk)
body = Polygon(Point(0,-100), Point(-20,-20), Point(20,-20))
body.setFillColor('darkgreen')
tree.add(body)
tree.setDepth(15)
win.add(tree)
tree.move(150, 240)

tree2 = tree.clone()
tree2.move(100, -10)
win.add(tree2)

ani = Layer()
head = Circle(10,Point(28,-5))
head.setFillColor('gold')
body = Rectangle(30,12)
body.move(15,1)
body.setFillColor('gold')
leg = Rectangle(4,20)
leg.setFillColor('gold')
leg.move(2,6)
leg2 = leg.clone()
leg2.move(3,0)
leg3 = leg.clone()
leg3.move(15,0)
leg4 = leg3.clone()
leg4.move(3,0)
ani.add(leg)
ani.add(leg2)
ani.add(leg3)
ani.add(leg4)
ani.add(body)
ani.add(head)
eye1 = Circle(2,Point(25,-7))
eye1.setFillColor('white')
eye2 = Circle(2,Point(32, -7))
eye2.setFillColor('white')
ani.add(eye1)
ani.add(eye2)
mouth = Path(Point(25,-1),Point(32,-1))
mouth.setBorderWidth(3)
mouth.setBorderColor('red')
ani.add(mouth)
ani.setDepth(5)
win.add(ani)
ani.move(-50,250)

ani2 = ani.clone()
ani2.move(0,-50)
ani2.setDepth(20)
win.add(ani2)

animate = Text("click to animate")
animate.moveTo(200,280)
win.add(animate)
grass.wait()
from time import sleep
for x in xrange(60):
    ani.move(1,0)
    tree.rotate(6)
    sleep(.04)

for x in xrange(210):
    ani.move(1,0)
    tree2.rotate(1.714)
    ani2.move(1,0)
    tree.rotate(-1.714)
    sleep(.04)

